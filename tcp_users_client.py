import socket

# =============================================================================
# СОЗДАНИЕ TCP-СОКЕТА
# =============================================================================
# socket.socket() - создаёт новый сокет (точку входа для сетевого взаимодействия)
# socket.AF_INET - используем IPv4 адресацию (например, 127.0.0.1)
# socket.SOCK_STREAM - используем TCP протокол (надёжная передача с установкой соединения)
client_socket = socket.socket(socket.AF_INET, socket.SOCK_STREAM)

# =============================================================================
# ПОДКЛЮЧЕНИЕ К СЕРВЕРУ
# =============================================================================
# Определяем адрес сервера:
# 'localhost' - сервер находится на этом же компьютере (эквивалент 127.0.0.1)
# 12345 - порт, на котором сервер ожидает подключения
server_address = ('localhost', 12346)

# connect() - устанавливает TCP-соединение с сервером
# Выполняет "трёхэтапное рукопожатие" (SYN, SYN-ACK, ACK)
client_socket.connect(server_address)

# =============================================================================
# ОТПРАВКА СООБЩЕНИЯ СЕРВЕРУ
# =============================================================================
# Сообщение для отправки (строка в кодировке UTF-8)
message = "Привет, сервер!"

# send() - отправляет данные серверу
# encode() - преобразует строку в байты (UTF-8), так как сеть работает с байтами
client_socket.send(message.encode())

# =============================================================================
# ПОЛУЧЕНИЕ ОТВЕТА ОТ СЕРВЕРА
# =============================================================================
# recv(1024) - получаем до 1024 байт данных от сервера
# Блокирует выполнение, пока данные не придут или соединение не закроется
# decode() - преобразует байты обратно в строку (UTF-8)
response = client_socket.recv(1024).decode()

# Выводим ответ сервера в консоль
print(f"Ответ от сервера: {response}")

# =============================================================================
# ЗАКРЫТИЕ СОЕДИНЕНИЯ
# =============================================================================
# close() - закрывает сокет и освобождает ресурсы
# Отправляет серверу сигнал о завершении соединения (FIN пакет)
client_socket.close()