import socket


def server():
    # 1. СОЗДАНИЕ СОКЕТА
    # socket.AF_INET - используем IPv4 адресацию
    # socket.SOCK_STREAM - используем TCP протокол (надёжная передача данных)
    server_socket = socket.socket(socket.AF_INET, socket.SOCK_STREAM)

    # 2. ПРИВЯЗКА К АДРЕСУ И ПОРТУ
    # 'localhost' - сервер будет доступен только с этого же компьютера
    # 12345 - порт, на котором сервер будет слушать входящие подключения
    server_socket.bind(('localhost', 12346))


    # 3. ПЕРЕХОД В РЕЖИМ ПРОСЛУШИВАНИЯ
    # 10 - максимальное количество клиентов в очереди ожидания
    server_socket.listen(10)

    # 4. ХРАНИЛИЩЕ СООБЩЕНИЙ
    # Список для сохранения всех полученных сообщений
    messages: list[str] = []

    # 5. БЕСКОНЕЧНЫЙ ЦИКЛ ОБРАБОТКИ КЛИЕНТОВ
    while True:
        # 5.1 ПРИНЯТИЕ ПОДКЛЮЧЕНИЯ
        # accept() блокирует выполнение до появления нового клиента
        # client_socket - сокет для обмена данными с конкретным клиентом
        # client_address - кортеж (IP, порт) клиента
        client_socket, client_address = server_socket.accept()
        print("Сервер запущен!")
        print(f"Пользователь с адресом: {client_address} подключился к серверу")

        # 5.2 ПОЛУЧЕНИЕ ДАННЫХ ОТ КЛИЕНТА
        # recv(1024) - получаем до 1024 байт данных
        # decode() - преобразуем байты в строку (UTF-8)
        message = client_socket.recv(1024).decode()
        print(f"Пользователь с адресом: {client_address} отправил сообщение: {message}")

        # 5.3 СОХРАНЕНИЕ СООБЩЕНИЯ
        # Добавляем полученное сообщение в общий список
        messages.append(message)

        # 5.4 ОТПРАВКА ВСЕХ СООБЩЕНИЙ КЛИЕНТУ
        # '\n'.join(messages) - объединяем все сообщения через перевод строки
        # encode() - преобразуем строку в байты для отправки
        client_socket.send('\n'.join(messages).encode())

        # 5.5 ЗАКРЫТИЕ СОЕДИНЕНИЯ С КЛИЕНТОМ
        # close() - освобождаем ресурсы, связанные с этим клиентом
        client_socket.close()
        # Сервер продолжает работу в цикле и ждёт следующего клиента


# Запуск сервера при прямом выполнении скрипта
if __name__ == '__main__':
    server()